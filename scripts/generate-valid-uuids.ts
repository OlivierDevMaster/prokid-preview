import { randomUUID } from 'crypto';
import { z } from 'zod';

const uuidSchema = z.uuid();

function generateValidUUID(): string {
  let uuid: string;
  do {
    uuid = randomUUID();
  } while (!uuidSchema.safeParse(uuid).success);
  return uuid;
}

